

#include <iostream>
using namespace std;
int main()
{
	int A = 100, B = 200;
	A += B;//A=A+B
	cout << "A=" << A << endl;
	A -= B;//A=A-B
	cout << "A=" << A << endl;
	A *= B;//A=A*B
	cout << "A=" << A << endl;
	A /= B;//A=A/B
	cout << "A=" << A << endl;
	A %= B;//A=A%B
	cout << "A=" << A << endl;



}

